/**
 * Obtiene los KPIs de un ejercicio para un usuario específico
 * @param {string|number} userId - ID del usuario
 * @param {string} exerciseName - Nombre del ejercicio
 * @returns {Object} - KPIs del ejercicio { affinityIndex, biomechanicalEfficiency }
 */
export const getExerciseKPI = (userId, exerciseName) => {
  const key = `${userId}_${exerciseName}`;
  return KPI_EXERCISES[key] || { affinityIndex: 0, biomechanicalEfficiency: 0 };
};

/**
 * Obtiene todos los KPIs de un usuario
 * @param {string|number} userId - ID del usuario
 * @returns {Object} - Objeto con todos los KPIs del usuario { exerciseName: { affinityIndex, biomechanicalEfficiency } }
 */
export const getUserKPIs = (userId) => {
  const userKPIs = {};
  const userPrefix = `${userId}_`;
  
  Object.keys(KPI_EXERCISES).forEach(key => {
    if (key.startsWith(userPrefix)) {
      const exerciseName = key.substring(userPrefix.length);
      userKPIs[exerciseName] = KPI_EXERCISES[key];
    }
  });
  
  return userKPIs;
};
